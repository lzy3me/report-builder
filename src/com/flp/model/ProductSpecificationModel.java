package com.flp.model;

public class ProductSpecificationModel {
	private int paperId;
	private String psInkOnFace;
	private String psInkBackFace;
	private String psHorzPerf;
	private String psVertPerf;
	private String psRemark;
	
	
	public int getPaperId() {
		return paperId;
	}
	public void setPaperId(int paperId) {
		this.paperId = paperId;
	}
	public String getPsInkOnFace() {
		return psInkOnFace;
	}
	public void setPsInkOnFace(String psInkOnFace) {
		this.psInkOnFace = psInkOnFace;
	}
	public String getPsInkBackFace() {
		return psInkBackFace;
	}
	public void setPsInkBackFace(String psInkBackFace) {
		this.psInkBackFace = psInkBackFace;
	}
	public String getPsHorzPerf() {
		return psHorzPerf;
	}
	public void setPsHorzPerf(String psHorzPerf) {
		this.psHorzPerf = psHorzPerf;
	}
	public String getPsVertPerf() {
		return psVertPerf;
	}
	public void setPsVertPerf(String psVertPerf) {
		this.psVertPerf = psVertPerf;
	}
	public String getPsRemark() {
		return psRemark;
	}
	public void setPsRemark(String psRemark) {
		this.psRemark = psRemark;
	}
	
}
