package com.flp.model;

public class PaperModel {
	private int paperId;
	private String paperType;
	private double paperWeight;
	private String paperColor;
	private double paperPrice;
	private String paperDefault;
	
	public int getPaperId() {
		return paperId;
	}
	public void setPaperId(int paperId) {
		this.paperId = paperId;
	}
	public String getPaperType() {
		return paperType;
	}
	public void setPaperType(String paperType) {
		this.paperType = paperType;
	}
	public double getPaperWeight() {
		return paperWeight;
	}
	public void setPaperWeight(double paperWeight) {
		this.paperWeight = paperWeight;
	}
	public String getPaperColor() {
		return paperColor;
	}
	public void setPaperColor(String paperColor) {
		this.paperColor = paperColor;
	}
	public double getPaperPrice() {
		return paperPrice;
	}
	public void setPaperPrice(double paperPrice) {
		this.paperPrice = paperPrice;
	}
	public String getPaperDefault() {
		return paperDefault;
	}
	public void setPaperDefault(String paperDefault) {
		this.paperDefault = paperDefault;
	}
}
